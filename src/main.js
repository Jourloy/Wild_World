function FirstGenerateWorld() {
    const coordinates = GetCoordinates(world);

    let players = {
        first: {
            verb: 't',
            stats: [],
        },
        second: {
            verb: 'e',
            stats: [],
        }
    };

    let y = 0;

    for (i in world) {
        if (world[i] == '\n') y++;
        if (world[i] == players.first.verb) {
            console.log({x: i - (y * coordinates.x), y: y, people: 2})
            players.first.stats.push[{x: i - (y * coordinates.x), y: y, people: 2}];
        }
        if (world[i] == players.second.verb) players.second.stats.push[{x: i - (y * coordinates.x), y: y, people: 2}];
    }

    console.log(players)
}

